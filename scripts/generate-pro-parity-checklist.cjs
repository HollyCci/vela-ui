#!/usr/bin/env node
const fs = require('fs');
const path = require('path');
const {
  REFERENCE,
  componentContracts,
  referenceVariantCounts,
  liveReferenceVariantCounts,
  browserSmokeComponents,
} = require('./pro-parity-contract.cjs');

const root = path.join(__dirname, '..');
const registryPath = path.join(root, 'src/showcase/registry.ts');
const demoIndexPath = path.join(root, 'src/showcase/demo-index.json');
const docsMetaPath = path.join(root, 'src/showcase/docs-meta.json');
const outputPath = path.join(root, 'docs/pro-parity-checklist.md');
const checkOnly = process.argv.includes('--check');

const registrySource = fs.readFileSync(registryPath, 'utf8');
const demoIndex = JSON.parse(fs.readFileSync(demoIndexPath, 'utf8'));
const docsMeta = JSON.parse(fs.readFileSync(docsMetaPath, 'utf8'));
const smokeIds = new Set(browserSmokeComponents);

const highRiskNotes = {
  agenda: 'Smoke covers view switching and event selection; still needs event drag, resize, and current-time indicator comparison.',
  'data-grid': 'Smoke covers sorting and selection; still needs cell editing, column resize, density, sticky, and virtual-scroll comparison.',
  'file-tree': 'Smoke covers tree slots and basic item interaction; still needs drag/drop and full keyboard navigation comparison.',
  'floating-toc': 'Smoke covers trigger behavior; still needs scroll-spy, anchor syncing, and sticky-state comparison.',
  kanban: 'Smoke covers board anatomy only; drag reorder, card actions, and column state still require live-reference comparison.',
  'list-view': 'Smoke covers multi-selection; still needs sorting, action bar, and empty-state comparison.',
  'number-value': 'Smoke covers formatting slots; still needs animated-change and locale option comparison.',
  'pressable-feedback': 'Smoke covers progress completion; still needs ripple, press duration, and keyboard feedback comparison.',
  rating: 'Smoke covers radio selection; still needs hover preview, readonly, and half-step comparison.',
  resizable: 'Smoke covers pointer resize; still needs keyboard resize, collapse, and nested group comparison.',
  'prompt-input': 'Smoke covers attachment submit; still needs autosize, stop/send states, queue reorder, suggestions, and dropzone comparison.',
  'rich-text-editor': 'Smoke covers text input and toolbar state; still needs each toolbar command, link popover, and character-count comparison.',
  'app-layout': 'Smoke covers sidebar/aside state; still needs mobile sheet, resizable areas, and sticky regions comparison.',
  command: 'Smoke covers dialog filtering and execution; still needs keyboard command navigation and grouped result comparison.',
  'context-menu': 'Smoke covers right-click action; still needs submenu, checkbox/radio items, and keyboard navigation comparison.',
  sidebar: 'Smoke covers mobile trigger; still needs collapse, nested navigation, and keyboard navigation comparison.',
  stepper: 'Smoke covers controlled step selection; still needs all orientation, status, action, and validation variants comparison.',
  sheet: 'Smoke covers open and Escape close; still needs placements, snap points, drag handles, and nested sheet comparison.',
};

const parseProCategories = () => {
  const proBlockMatch = /export const PRO_CATEGORIES:[\s\S]*?=\s*\{([\s\S]*?)\};/.exec(registrySource);
  if (!proBlockMatch) {
    throw new Error('Unable to find PRO_CATEGORIES in src/showcase/registry.ts');
  }

  const categories = [];
  const categoryRe = /['"]?([A-Za-z ]+)['"]?\s*:\s*\[([\s\S]*?)\]/g;
  let categoryMatch;
  while ((categoryMatch = categoryRe.exec(proBlockMatch[1])) !== null) {
    const category = categoryMatch[1].trim();
    const ids = [...categoryMatch[2].matchAll(/'([a-z0-9-]+)'/g)].map((match) => match[1]);
    categories.push({ category, ids });
  }
  return categories;
};

const formatBehavior = (id) => {
  const tags = componentContracts[id] ?? [];
  return tags.map((tag) => `\`${tag}\``).join(', ');
};

const formatTitle = (title) => title.replace(/New$/, ' (New)');

const formatManualState = () => '`pending`';

const formatOpenGap = (id, localCount, referenceCount) => {
  if (referenceCount !== undefined && localCount !== referenceCount) {
    return `Variant count mismatch: local ${localCount}, reference ${referenceCount}.`;
  }
  return highRiskNotes[id] ?? 'Needs same-viewport visual, DOM/CSS, and remaining interaction comparison against the live reference.';
};

const componentPage = (id) => `${REFERENCE.url}/${id}`;

const buildMarkdown = () => {
  const categories = parseProCategories();
  const lines = [];
  lines.push('# HeroUI Pro Parity Checklist');
  lines.push('');
  lines.push('> Generated by `pnpm generate:pro-checklist`. Edit the contract data or script, then regenerate this file.');
  lines.push('');
  lines.push(`Reference: [${REFERENCE.name} ${REFERENCE.version}](${REFERENCE.url})`);
  lines.push(`Reference checked: ${REFERENCE.checkedAt}`);
  lines.push('');
  lines.push('## Completion Contract');
  lines.push('');
  lines.push('- `Browser smoke` means the local showcase page renders real Vela components and exercises at least one meaningful browser interaction for the component.');
  lines.push('- `Manual visual`, `Manual DOM/CSS`, and `Manual interaction` stay `pending` until a same-viewport comparison against the live HeroUI Pro page has been performed and recorded.');
  lines.push('- This checklist is a progress artifact, not completion proof. The goal remains open while any row has pending manual parity work or an open gap.');
  lines.push('');
  lines.push('## Required Gates');
  lines.push('');
  lines.push('- `pnpm test`');
  lines.push('- `pnpm type-check`');
  lines.push('- `pnpm build`');
  lines.push('- `pnpm build:demo`');
  lines.push('- `pnpm audit:goal`');
  lines.push('- `pnpm audit:pro:reference-live` before claiming final parity; this external-network gate is intentionally not part of `audit:goal`.');
  lines.push('');
  lines.push('## Component Checklist');

  for (const { category, ids } of categories) {
    lines.push('');
    lines.push(`### ${category}`);
    lines.push('');
    lines.push('| Component | Live reference variants | Local demos | Behavior contract | Browser smoke | Manual visual | Manual DOM/CSS | Manual interaction | Open gap |');
    lines.push('| --- | ---: | ---: | --- | --- | --- | --- | --- | --- |');
    for (const id of ids) {
      const referenceCount = liveReferenceVariantCounts[id] ?? referenceVariantCounts[id];
      const localCount = demoIndex[id]?.length ?? 0;
      const title = formatTitle(docsMeta[id]?.title ?? id);
      const smoke = smokeIds.has(id) ? '`covered`' : '`missing`';
      const row = [
        `[${title}](${componentPage(id)})`,
        referenceCount ?? '`missing`',
        localCount,
        formatBehavior(id),
        smoke,
        formatManualState(),
        formatManualState(),
        formatManualState(),
        formatOpenGap(id, localCount, referenceCount),
      ];
      lines.push(`| ${row.join(' | ')} |`);
    }
  }

  lines.push('');
  return `${lines.join('\n')}`;
};

const content = buildMarkdown();

if (checkOnly) {
  const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : '';
  if (existing !== content) {
    console.error('docs/pro-parity-checklist.md is stale. Run `pnpm generate:pro-checklist`.');
    process.exitCode = 1;
  } else {
    console.log('pro parity checklist ok');
  }
} else {
  fs.writeFileSync(outputPath, content);
  console.log(`wrote ${path.relative(root, outputPath)}`);
}
